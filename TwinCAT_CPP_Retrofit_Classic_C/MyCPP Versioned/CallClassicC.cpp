// State transition from PREOP to SAFEOP
//
// Initialize input parameters 
// Allocate memory
HRESULT CCallClassicC::SetObjStatePS(PTComInitDataHdr pInitData)
{
	m_Trace.Log(tlVerbose, FENTERA_STR());
	HRESULT hr = S_OK;
	IMPLEMENT_ITCOMOBJECT_EVALUATE_INITDATA(pInitData);

	// query TcCOM object server for ITcADI interface with own object id, 
	// which retrieves a reference to the TMC module instance handler
	m_spADI.SetOID(m_objId);
	hr = FAILED(hr) ? hr : m_spSrv->TcQuerySmartObjectInterface(m_spADI);

	// Fall back to sensible defaults if the instance has no gains configured
	if (m_Parameter.Kp == 0 && m_Parameter.Ki == 0 && m_Parameter.Kd == 0)
	{
		m_Parameter.Kp = DEFAULT_KP;
		m_Parameter.Ki = DEFAULT_KI;
		m_Parameter.Kd = DEFAULT_KD;
	}
	if (m_Parameter.TempHighLimit == 0)
	{
		m_Parameter.TempHighLimit = DEFAULT_HIGH_LIMIT;
	}

	// Initialize the legacy controller core (plain C calls)
	pid_init(&m_Ctrl.pid, m_Parameter.Kp, m_Parameter.Ki, m_Parameter.Kd, 0.0, 100.0);

	tseq_cfg_t seqCfg;
	seqCfg.in_band       = 1.0;    // "at temperature" within +/-1 degC
	seqCfg.out_band      = 3.0;    // fall back to RAMP beyond +/-3 degC
	seqCfg.in_band_ticks = 500;    // in band for 500 cycles before TRACK
	seqCfg.high_limit    = m_Parameter.TempHighLimit;
	seqCfg.trip_ticks    = 100;    // 100 cycles above limit trips FAULT
	seqCfg.reset_margin  = 10.0;   // must cool 10 degC below limit to reset
	tseq_init(&m_Ctrl.seq, &seqCfg);

	m_Ctrl.plantTemp = PLANT_AMBIENT;

	m_Trace.Log(tlVerbose, FLEAVEA_STRV("hr=0x%08x", hr));
	return hr;
}

