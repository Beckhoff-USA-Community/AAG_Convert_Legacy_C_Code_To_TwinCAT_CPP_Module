///<AutoGeneratedContent id="ImplementationOf_ITcCyclic">
HRESULT CCallClassicC::CycleUpdate(ITcTask* ipTask, ITcUnknown* ipCaller, ULONG_PTR context)
{
	HRESULT hr = S_OK;

	// task cycle time -> seconds (the legacy code's "tick")
	ULONG cycleTimeNs = 0;
	double dt = 0.001;
	if (SUCCEEDED(ipTask->GetCycleTime(&cycleTimeNs)) && cycleTimeNs > 0)
	{
		dt = cycleTimeNs * 1e-9;
	}

	// ---- legacy C code, called unchanged --------------------------------
	tseq_state_t state = tseq_step(&m_Ctrl.seq,
		m_pInputs->Enable != 0, m_pInputs->FaultReset != 0,
		m_pInputs->SetpointTemp, m_Ctrl.plantTemp);

	double power = 0.0;
	if (tseq_heater_allowed(&m_Ctrl.seq))
	{
		power = pid_step(&m_Ctrl.pid, m_pInputs->SetpointTemp, m_Ctrl.plantTemp, dt);
	}
	else
	{
		pid_reset(&m_Ctrl.pid);
	}
	// ---------------------------------------------------------------------

	// simulated heater plant (first-order lag), stands in for the real I/O
	m_Ctrl.plantTemp += dt / PLANT_TAU * (PLANT_AMBIENT + PLANT_GAIN * power - m_Ctrl.plantTemp);

	m_pOutputs->ActualTemp  = m_Ctrl.plantTemp;
	m_pOutputs->HeaterPower = power;
	m_pOutputs->CtrlState   = (ULONG)state;
	m_pOutputs->AlarmFlags  = tseq_alarms(&m_Ctrl.seq);

	m_Counter++;

	return hr;
}
